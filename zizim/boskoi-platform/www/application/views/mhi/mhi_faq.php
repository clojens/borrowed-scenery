                <p>
                	<a name="1"></a>
                	<h4>Can I have multiple accounts?</h4>
                	Yes. Keep in mind that you can have multiple deployments with one account, though!
                </p>
                
                <p>
                	<a name="2"></a>
                	<h4>Is there a storage limit?</h4>
                	While we aren't enforcing a strict limit on storage at this time, we are monitoring deployments and maintain the right to take action to prevent any one single deployment from hogging resources.
                </p>
                
                <p>
                	<a name="3"></a>
                	<h4>How does Crowdmap work with SMS reports?</h4>
                	Your deployment will not automatically support SMS immediately after you create your deployment. To get this to work, you must create an account at <a href="www.clickatell.com">Clickatell</a> or set up <a href="FrontlineSMS">FrontlineSMS</a> to work with your deployment. To get it to work with your deployment, you will need to follow the instructions in the admin panel of your deployment. These instructions can be found here: http://[YOURDEPLOYMENT].crowdmap.com/admin/settings/sms<br/><br/>If you are looking for non-technical information regarding the approval and verification of SMS messages, check out, <a href="http://ushahidi.com/media/c_Ushahidi-Verification-Guide.pdf">"A Guide To Verification"</a> on the <a href="http://ushahidi.com/community_resources">Ushahidi Community Resources</a> page.
                </p>
                
                <p>
                	<a name="4"></a>
                	<h4>How do I reset my password?</h4>
                	Go to the <a href="http://crowdmap.com/mhi/reset_password">Password Reset</a> page and submit your email address. We will email you instructions on how to change your password.
                </p>
                
                <p>
                	<a name="5"></a>
                	<h4>Can I use my own domain name?</h4>
                	Currently we are not offering services to bind your domain name to your Crowdmap deployment. We are looking into offering this in the future so keep your eyes open.
                </p>
                
                <p>
                	<a name="6"></a>
                	<h4>Can I advertise on my deployment?</h4>
                	Sure.
                </p>
                
                <p>
                	<a name="7"></a>
                	<h4>How do I change my username, password or name?</h4>
                	Log into your account and go to the <a href="http://crowdmap.com/mhi/account">Account Settings</a>. From here you are able to change your email address, which is also your username. You are also able to change your password and your name.
                </p>
                
                <p>
                	<a name="8"></a>
                	<h4>What can kinds of things can I use my map for?</h4>
                	To see what some other people and organizations have done with Ushahidi. We have curated a <a href="http://ushahidi.com/platform">list of deployments</a> on the Ushahidi website so you can get some ideas.
                </p>
                
                <p>
                	<a name="9"></a>
                	<h4>Will this work with Ushahidi plugins?</h4>
                	As of right now, Crowdmap does not offer support for plugins. However, this is something we are very excited about and will be providing approved plugins for your use in the future!
                </p>
                
                <p>
                	<a name="10"></a>
                	<h4>How can I track my stats?</h4>
                	You will be able to check statistics in the admin panel of your deployment. Just visit http://[YOURDEPLOYMENT].crowdmap.com/admin/stats to see these stats. If you would like to use a service like Google Analytics, you will be able to put the web property ID provided to you by Google Analytics on the settings page in the admin panel. This is located at http://[YOURDEPLOYMENT].crowdmap.com/admin/settings/site. Visit <a href="http://www.google.com/analytics/">Google Analytics</a> for more information.
                </p>
                
